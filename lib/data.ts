export function getConceptById(id: string): Concept | undefined {
  return concepts.find((c) => c.id === id);
}

export function getRandomConcepts(count: number): Concept[] {
  const shuffled = [...concepts].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

// For journal: pick a daily concept question based on the date
export function getDailyConceptForDate(dateStr: string): Concept {
  // Deterministic: same date → same concept
  const dayIndex =
    dateStr
      .split("-")
      .map(Number)
      .reduce((a, b) => a * 31 + b, 0) % concepts.length;
  return concepts[Math.abs(dayIndex) % concepts.length];
}
